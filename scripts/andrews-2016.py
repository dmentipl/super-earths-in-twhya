def convol2df(im,psf):
    '''
    From C Pinte.
    '''

    psf2 = im * 0.

    dx=im.shape[0]
    dy=im.shape[1]

    dx2=psf.shape[0]
    dy2=psf.shape[0]

    startx = int((dx-dx2)/2)
    starty = int((dy-dy2)/2)

    psf2[startx:startx+dx2,starty:starty+dy2] = psf

    fim = np.fft.fft2(im)
    fpsf = np.fft.fft2(psf2)

    fim = fim * fpsf

    im = np.fft.ifft2(fim)
    im = np.roll(im,(int(dx/2),int(dy/2)),(0,1))
    im = abs(im) / (dx*dy)

    return im


