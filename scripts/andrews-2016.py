def Tbrightness_to_Flux(T, wl, BMAJ, BMIN):
    '''
    From C. Pinte

    Convert brightness temperature [K] to flux density [mJy]
        T [K]
        wl [cm]
        BMAJ, BMIN in [mas]
        flux [mJy]
    '''

    # Frequency.
    nu = c/wl

    # Calculate flux.
    flux = 2*h*nu**3/(c**2) / (np.exp(h*nu/(kB*T)) - 1)

    # Convert beam to radians.
    BMAJ = BMAJ * mas
    BMIN = BMIN * mas

    # Convert flux to mJy.
    flux = flux / (Jy/1000)
    flux = flux * (BMIN*BMAJ * np.pi/4/np.log(2))

    return flux


